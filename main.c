unsigned char hasCollided( STyp s1, STyp s2 )
{
	// if Y1 > Y2
	if( s1.y >= s2.y )
	{
		// Does Y2 falls within (Y1,Y1+H1) ?
		if( s2.y+1 > s1.y-s1.h )
			// Y indicates collision, check X2 fall within (X1,X1+W1)
			if( s1.x < s2.x )
			{
				// X2 falls within X1+W1 ?
				if( s2.x+1 < s1.x+s1.w ) return 1;
			} else 
				if( s1.x+1 < s2.x+s2.w ) return 1;
	} else
	{
		if( s1.y+1 > s2.y-s2.h )
			// Y indicates collission, check X
			if( s1.x < s2.x )
			{
				if( s2.x+1 < s1.x+s1.w ) return 1;
			} else
				if( s1.x+1 < s2.x+s2.w ) return 1;
	}
	// no collision detected
	return 0;
}


// returns 0 when no collision is detected
// returns n for sprite affected
unsigned char hasAllienCollided( STyp spr1 )
{
	long n;
	for(n=0; n < ALLIEN_NUM; n++)
		if(	allien[n].status == ALIVE && hasCollided(spr1, allien[n]) )
			return n+1;
		
	return 0;
}

// returns 0 when no collision is detected
// returns n for sprite affected
unsigned char hasMissileCollided( STyp s1 )
{
	long n;
	for(n=0; n < MAX_MISSILES; n++)
		if(	missile[n].status == ALIVE && hasCollided(s1, missile[n]) )
			return n+1;
	return 0;
}

