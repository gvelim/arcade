void MoveShip()
{
	switch( pship.status )
	{
		case ALIVE:		
			if( isLeftPressed() && pship.x > 1)
				pship.x--;
			if( isRightPressed() && pship.x < SCREENW-pship.w-1)
				pship.x++;
			break;
		
		case DAMAGED:
			pship.decay--;
			if( pship.decay == 0 ) pship.status = DEAD;
			
			pship.img_count = pship.decay & 0x01;
		
		default:
			break;
	}
}

